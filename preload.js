const { contextBridge, ipcRenderer } = require('electron');
contextBridge.exposeInMainWorld('api', {
  readdir: p => ipcRenderer.invoke('fs:readdir', p),
  listImages: p => ipcRenderer.invoke('fs:listImages', p),
  scanImages: (p, d) => ipcRenderer.invoke('fs:scanImages', p, d),
  getThumb: p => ipcRenderer.invoke('fs:getThumb', p),
  getVideoThumb: p => ipcRenderer.invoke('fs:getVideoThumb', p),
  getCachedThumbs: paths => ipcRenderer.invoke('fs:getCachedThumbs', paths),
  imageUrl: p => ipcRenderer.invoke('fs:imageUrl', p),
  homedir: () => ipcRenderer.invoke('fs:homedir'),
  quickPaths: () => ipcRenderer.invoke('fs:quickPaths'),
  openFile: p => ipcRenderer.invoke('fs:openFile', p),
  trash: p => ipcRenderer.invoke('fs:trash', p),
  rename: (o,n) => ipcRenderer.invoke('fs:rename', o, n),
  copy: (s,d) => ipcRenderer.invoke('fs:copy', s, d),
  copyFile: (s,d) => ipcRenderer.invoke('fs:copyFile', s, d),
  mkdir: p => ipcRenderer.invoke('fs:mkdir', p),
  stat: p => ipcRenderer.invoke('fs:stat', p),
  permanentDelete: paths => ipcRenderer.invoke('fs:permanentDelete', paths),
  openTerminal: p => ipcRenderer.invoke('fs:openTerminal', p),
  openPhotos: (f, img, sorted) => ipcRenderer.invoke('app:openPhotos', f, img, sorted),
  getSortedList: () => ipcRenderer.invoke('photos:getSortedList'),
  nativeDrag: p => ipcRenderer.send('native-drag', p),
  clipSet: d => ipcRenderer.invoke('clip:set', d),
  clipGet: () => ipcRenderer.invoke('clip:get'),
  loadCfg: (n, def) => ipcRenderer.invoke('cfg:load', n, def),
  saveCfg: (n, d) => ipcRenderer.invoke('cfg:save', n, d),
  sessionSave: d => ipcRenderer.invoke('session:save', d),
  sessionLoad: () => ipcRenderer.invoke('session:load'),
  netMount: l => ipcRenderer.invoke('net:mount', l),
  netScan: () => ipcRenderer.invoke('net:scan'),
  netTest: h => ipcRenderer.invoke('net:test', h),
  netListMounted: () => ipcRenderer.invoke('net:listMounted'),
  winMin: () => ipcRenderer.invoke('win:min'),
  winMax: () => ipcRenderer.invoke('win:max'),
  winClose: () => ipcRenderer.invoke('win:close'),
  pickFolder: () => ipcRenderer.invoke('dialog:pickFolder'),
});
